//SÍNCRONO BLOQUEANTE:
//Función anónima autoejecutable:
(() => {
  console.log("01 Inicio");

  function dos(){
    console.log("03 Dos");
  }

  function uno(){
    console.log("02 Uno");
    dos();
  }

  uno();
  console.log("04 Fin");
})();
//------------------------------------------------------------------------------------------------------------------------------------------//

//------------------------------------------------------------------------------------------------------------------------------------------//
//ASÍNCRONO NO BLOQUEANTE:
(() => {
  console.log("01 Inicio");

  function dos(){
    setTimeout(() => {
      console.log("04 Dos");
    }, 1000);
  }

  function uno(){
    setTimeout(() => {
      console.log("03 Uno");
    }, 0);

    dos();
  }

  uno();
  console.log("02 Fin");
})();
